//LOCAL STORAGE
//ALMACENA DE LA SIGUIENTE MANERA --> KEY-VALOR (STRING-STRING)
//LA KEY ES EL NOMBRE QUE LE ASIGNAMOS Y EL VALOR ES EL VALOR PORPIAMENTE DICHO
//TIENE DOS METODOS

//SET ITEM --> ES PARA CREAR NUEVO REGISTRO
//GET ITEM --> ES PARA OBTENER UN REGISTRO

//GUARDAR UN REGISTRO EN LOCAL STORAGE LS

localStorage.setItem("TEST","PROBANDO EL ALMACENANMIENTO EN EL LOCAL STORAGE")
//KEY = "TEST"
//VALOR = "PROBANDO EL ALMACENAMIENTO EN EL LOCAL STORAGE"


//OBTENER UN REGISTRO DEL LOCAL STORAGE LS
//MUESTRA EL VALOR, LA KEY ES PARA IDENTIFICARLO

console.log(localStorage.getItem("TEST"));

//MODIFICAR EL VALOR DE UN REGISTROS YA CREADO

localStorage.setItem("TEST", "PROBANDO LA MODIFICACION");
console.log(localStorage.getItem("TEST"));

//ELIMINAR UN REGISTRO DEL LS

localStorage.removeItem("TEST");

