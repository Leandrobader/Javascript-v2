//JSON Javascript objets notation
//NOS PROPORCIONA DOS METODOS 
//1- PARA TRANSFORMAR UN OBJETO O ARRAY EN STRING
//2-PARA HACER EL STRING EN OBJETO O ARRAY 

//CONVERTIR EL ARRAY DE OBJETOS EN UN STRING PARA PODER GUARDAR EN EL LOCAL STORAGE

